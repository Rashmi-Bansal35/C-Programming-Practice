/*
Show the following patterns just using print statements:
i) Right Half Pyramid  |  ii) Reverse Right Half Pyramid  | iii) Left Half Pyramid
    *                  |      *****                       |           *
    **                 |      ****                        |          **
    ***                |      ***                         |         ***
    ****               |      **                          |        ****
    *****              |      *                           |       *****
*/

#include<stdio.h>

int main(){
    printf("i) Right Half Pyramid\n");
    printf("*\n");
    printf("**\n");
    printf("***\n");
    printf("****\n");
    printf("*****\n");

    printf("\nii) Reverse Right Half Pyramid\n");
    printf("*****\n");
    printf("****\n");
    printf("***\n");
    printf("**\n");
    printf("*\n");

    printf("\niii) Left Half Pyramid\n");
    printf("    *\n");
    printf("   **\n");
    printf("  ***\n");
    printf(" ****\n");
    printf("*****\n");
    return 0;
}
